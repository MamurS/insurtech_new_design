// Helper to get section for a SIC code
export const getSectionForCode = (sicCode: string): SICSection | undefined => {
  const entry = SIC_CODES.find(c => c.code === sicCode);
  if (!entry) return undefined;
  return SIC_SECTIONS.find(s => s.code === entry.section);
};

// Helper to format display string
export const formatSICDisplay = (sicCode: string): string => {
  const entry = SIC_CODES.find(c => c.code === sicCode);
  if (!entry) return sicCode;
  return `${entry.code} - ${entry.description}`;
};
